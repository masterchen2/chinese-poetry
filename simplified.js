/**
 * 将繁体字转换为简体字
 * @param {string} text - 需要转换的文本
 * @returns {string} - 转换后的文本
 */
function convertToSimplified(text) {
    if (!text) return '';
    
    let result = '';
    for (let i = 0; i < text.length; i++) {
        const char = text[i];
        result += traditionalToSimplified[char] || char;
    }
    
    return result;
}

/**
 * 将文本数组中的繁体字转换为简体字
 * @param {string[]} textArray - 需要转换的文本数组
 * @returns {string[]} - 转换后的文本数组
 */
function convertArrayToSimplified(textArray) {
    if (!textArray || !Array.isArray(textArray)) return [];
    
    return textArray.map(text => convertToSimplified(text));
}